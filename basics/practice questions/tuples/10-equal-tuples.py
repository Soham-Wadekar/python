# 10. Write a program to check if two tuples are equal.

tup1 = (1,2,3,4,5,6)
tup2 = (4,5,6,7,8,9)

tup3 = (1,2,3)
tup4 = (1,2,3)

print(f"The tuples {tup1} and {tup2} are equal" if tup1==tup2 else f"The tuples {tup1} and {tup2} are not equal")

print(f"The tuples {tup3} and {tup4} are equal" if tup3==tup4 else f"The tuples {tup3} and {tup4} are not equal")